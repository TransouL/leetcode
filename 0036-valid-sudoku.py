from typing import List


class Solution:
    def isValidSudoku(self, board: List[List[str]]) -> bool:
        rows = [[False] * 9 for _ in range(9)]
        columns = [[False] * 9 for _ in range(9)]
        subboxs = [[[False] * 9 for _ in range(3)] for _ in range(3)]
        for i in range(9):
            for j in range(9):
                ch = board[i][j]
                if ch != '.':
                    idx = ord(ch) - ord('0') - 1
                    if rows[i][idx] or columns[j][idx] or subboxs[i // 3][j // 3][idx]:
                        return False
                    rows[i][idx] = columns[j][idx] = subboxs[i // 3][j // 3][idx] = True
        return True


